	struct Triangle
	{
		public Vector3 a;
		public Vector4 colorA;
		public float iluminacionA;

		public Vector3 b;
		public Vector4 colorB;
		public float iluminacionB;

		public Vector3 c;
		public Vector4 colorC;
		public float iluminacionC;

		public Vector3 normal;

		public Vector3 this[int i]
		{
			get
			{
				switch (i)
				{
					case 0:
						return a;
					case 1:
						return b;
					default:
						return c;
				}
			}
		}

		public Color GetColor(int i)
        {
			switch (i)
			{
				case 0:
					return colorA;
				case 1:
					return colorB;
				default:
					return colorC;
			}
		}

		public Vector3 GetNormal()
        {
			return normal;
        }

        public  Vector2 GetIluminacion(int i)
        {
			Vector2 uv = new Vector2(0, 0);
			switch (i)
			{
				case 0:
					uv.x = iluminacionA; break;
				case 1:
					uv.x = iluminacionB; break;
				default:
					uv.x = iluminacionC; break;
			}
			return uv;
		}
    }

	struct Dato
    {
		Vector3Int posicion;
		float valor;
		Color color;
		float iluminacion;

		public Dato(Vector3Int posicion, float valor, Color color, float iluminacion)
        {
			this.posicion = posicion;
			this.valor = valor;
			this.color = color;
			this.iluminacion = iluminacion;
        }
