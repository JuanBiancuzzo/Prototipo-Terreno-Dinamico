	private void GenerarCubo(int x, int y, int z, Func<List<Vector3Int>, float[]> ValoresDeCubo)
    {
		List<Vector3Int> posicionesCubo = PosicionesDelCubo(x, y, z);
		float[] valores = ValoresDeCubo(posicionesCubo);
		int indexCubo = Utilidades.CubeIndex(valores, m_nivelDelSuelo);

		List<int> indexTriangulos = new List<int>();
		for (int i = 0; TriangulationTable[indexCubo, i] != -1; i++)
			indexTriangulos.Add(TriangulationTable[indexCubo, i]);

		int contador = 0;
		for (int i = 0; i < indexTriangulos.Count; i++)
        {
			int index1 = CornerIndexFromEdge[indexTriangulos[i], 0];
			int index2 = CornerIndexFromEdge[indexTriangulos[i], 1];

			Vector3 vectice = VectorMedio(posicionesCubo[index1], valores[index1], posicionesCubo[index2], valores[index2]);

			if (m_meshData.m_repetirVertices.ContainsKey(vectice))
			{
				m_meshData.m_triangulos.Add(m_meshData.m_repetirVertices[vectice]);
			}
			else
			{
				m_meshData.m_triangulos.Add(m_meshData.m_vertices.Count);
				m_meshData.m_repetirVertices.Add(vectice, m_meshData.m_vertices.Count);
				m_meshData.m_vertices.Add(vectice);
				m_meshData.m_normales.Add(new Vector3(0, 0, 0));
			}
			
			if (contador == 3) // cuando se hizo un triangulo
            {
				int indexA = m_meshData.m_triangulos[m_meshData.m_vertices.Count - 3];
				int indexB = m_meshData.m_triangulos[m_meshData.m_vertices.Count - 2];
				int indexC = m_meshData.m_triangulos[m_meshData.m_vertices.Count - 1];

				Vector3 verticeA = m_meshData.m_vertices[indexA];
				Vector3 verticeB = m_meshData.m_vertices[indexB];
				Vector3 verticeC = m_meshData.m_vertices[indexC];

				Vector3 normal = Vector3.Cross(verticeB - verticeA, verticeC - verticeA).normalized;

				m_meshData.m_normales[indexA] += normal;
				m_meshData.m_normales[indexB] += normal;
				m_meshData.m_normales[indexC] += normal;
			}

			contador = (contador + 1) % 3;
		}
	}

	private Vector3 VectorMedio(Vector3 posicionA, float valorA, Vector3 posicionB, float valorB)
    {
		float valorMedio = Mathf.InverseLerp(valorA, valorB, m_nivelDelSuelo);
		return Vector3.Lerp(posicionA, posicionB, Mathf.Clamp(valorMedio, 0f, 1f));
    }

	private List<Vector3Int> PosicionesDelCubo(int x, int y, int z)
    {
		List<Vector3Int> posiciones = new List<Vector3Int>();
		for (int j = 0; j <= 1; j++)
			for (int k = 0; k <= 1; k++)
				for (int u = 0; u <= 1; u++)
				{
					int i = u;
					if (k == 0)
						i = (u == 0) ? 1 : 0;
					posiciones.Add(new Vector3Int(x + i, y + j, z + k));
				}
		return posiciones;
	}

